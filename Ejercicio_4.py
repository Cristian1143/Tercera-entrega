# Programa para calcular el área de un rectángulo

# Pedimos al usuario que ingrese la altura y la anchura
altura = float(input("Ingrese la altura del rectángulo (en metros): "))
anchura = float(input("Ingrese la anchura del rectángulo (en metros): "))

# Calculamos el área
area = altura * anchura

# Mostramos el resultado
print("El área del rectángulo es:", area, "metros cuadrados")
